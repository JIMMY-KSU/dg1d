    """Returns the coefficients for the Kutta's third order method"""

    # Bottom row of Butcher table
    coeffs = [1. / 6., 2. / 3., 1. / 6.]

    # Left vertical column of Butcher table
    alphas = [0.0, 0.5, 1.0]

    # Main matrix of Butcher table
